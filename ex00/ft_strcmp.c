int	ft_strcmp(char *s1, char *s2)
{
	int	result;

	while (*s1 == *s2)
	{
		s1++;
		s2++;
		if (*s1 == '\0' && *s2 == '\0')
		{
			return (0);
		}
	}
	result = *s1 - *s2;
	return (result);
}
